    <h2>Technopark</h2>
    <p>
    Technopark merupakan sebuah kawasan yang dikelola oleh organisasi -organisasi dengan profesional dan mendorong terciptanya inovasi untuk meningkatkan kesejahteraan anggotanya.
    <br>
    SMK Negeri 2 BUDURAN TECHNO PARK adalah wadah bagi start up yang didirikan oleh siswa sebagai bagian dari proses pembelajaran siswa agar kompetensi siswa bisa seiring sejalan dengan dunia usaha dan dunia industri.
    <br>
    Semua produk dari start up akan di taruh di etalase TECHNO PARK sehingga masyarakat bisa lebih mudah untuk mencari produk yang sesuai dengan kebutuhannya.
