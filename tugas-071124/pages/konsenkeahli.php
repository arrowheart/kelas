    <h4>Rekayasa Perangkat Lunak</h4>
    <img src="images/rpl.png" alt="">
    <p>
    Rekayasa Perangkat Lunak (RPL) merupakan bidang ilmu yang mempelajari 
    tentang pengembangan perangkat lunak yang meliputi pembuatan, pemeliharaan 
    dan menejemen organisasi pengembangan perangkat lunak serta  menejemen kualitasnya.
    Rekayasa perangkat lunak adalah pengubahan perangkat lunak itu sendiri guna mengembangkan, 
    memelihara, dan membangun kembali dengan menggunakan prinsip rekayasa untuk menghasilkan 
    perangkat lunak yang dapat bekerja lebih efisien dan efektif untuk pengguna.
    </p>

    <h4>Desain Komunikasi Visual</h4>
    <img src="images/dkv.png" alt="">
    <p>
    Program Keahlian Desain Komunikasi Visual (DKV) adalah cabang ilmu desain yang mempelajari 
    konsep komunikasi dan ungkapan kreatif, teknik dan media dengan memanfaatkan elemen-elemen 
    visual ataupun rupa untuk menyampaikan pesan untuk tujuan tertentu (tujuan informasi ataupun 
    tujuan persuasi yaitu mempengaruhi perilaku). Banyak hal-hal mendasar yang dipelajari di bidang DKV. 
    Mengembangkan bentuk bahasa visual (bermain gambar), mengolah pesan (bermain kata) keduanya untuk tujuan 
    sosial maupun komersial, dari individu atau kelompok yang ditujukan kepada kelompok lainnya. 
    Visual berwujud kreatif dan inovatif, sementara inti pesan harus komunikatif, efisien dan efektif 
    saling mendukung agar tersampaikan dengan baik pada sasaran.
    </p>

    <h4>Layanan PerBankan</h4>
    <img src="images/lpb.png" alt="">
    <p>
    Layanan Perbankan adalah salah satu kompetensi keahlian dari Program Keahlian Akuntansi dan Keuangan  
    Lembaga pada Bidang Keahlian Bisnis dan Manajemen.  Hampir mirip dengan Kompetensi keahlian Akuntansi, 
    Kompetensi keahlian Perbankan juga banyak membekali siswanya dengan materi hitung-menghitung. 
    Namun bedanya jurusan perbankan akan lebih berkonsentrasi pada seluruh kegiatan yang berhubungan 
    dengan bank seperti akuntansi perbankan, komputer akuntansi perbankan, customer service, teller dan 
    administrasi kredit  hingga pelaporan keuangan Bank.
    </p>
